package prework;
public class ConditionalExecutionFlow{
  
  public static void main(String[] args){
  
    int i = 10;
    System.out.println("i is " + i);
    
    if (i == 10){
    
      int j = 20;
      System.out.println("j is " + j);
      
    } else{
    
      int k = 30;
      System.out.println("k is " + k);
      
    }
  }
}